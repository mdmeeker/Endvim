vim.opt_local.tabstop = 4
vim.opt_local.shiftwidth = 4

-- Use 2 spaces for lists
vim.opt_local.list = true
vim.opt_local.listchars = "2s"

-- Set visual column marker at 92 characters
vim.opt.colorcolumn = "92"
